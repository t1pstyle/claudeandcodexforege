"""
Сборка промпта для GPT-4o.

КЛЮЧЕВАЯ ИДЕЯ — порядок частей для prompt cache:
    [system prompt (~500 tok, static)]       ← одинаковый ВСЕГДА
    [criteria (~3000-6000 tok, per type)]    ← одинаковый для данного task_type
    [task data (~500-1500 tok, per variant)] ← одинаковый для данного конкретного задания
    [transcript (unique, short)]              ← уникальный на каждого ученика

OpenAI автоматически кэширует совпадающий префикс (>= 1024 токенов) на 5-10 мин.
Чем больше пользователей делают одно и то же задание — тем выше cache hit rate.
Транскрипт и инструкция "верни JSON по схеме" идут В КОНЦЕ, чтобы не ломать
кэш первых трёх блоков.

Выходной JSON строго детерминирован — схема зависит от task_type. Мы используем
response_format={"type": "json_object"} + явное описание схемы в prompt.
"""
