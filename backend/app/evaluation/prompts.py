# ----------------------------------------------------------------------------
# Сборщик user-message промпта.
# ----------------------------------------------------------------------------

def _render_task_data(task: ExamTask) -> str:
    """Превращает ExamTask в текстовый блок для промпта."""
    lines = [
        f"# Условие задания {task.task_number}",
        "",
        f"Тип задания: {task.task_type.value}",
        f"Время на подготовку: {task.prep_seconds} секунд",
        f"Время на ответ: {task.speak_seconds} секунд",
        "",
        "## Инструкция ученику",
        task.prompt_text.strip(),
    ]
    if task.support_material:
        lines += [
            "",
            "## Дополнительный материал (то, что ученик видит на экране)",
            task.support_material.strip(),
        ]
    if task.image_url:
        lines += ["", f"[Фото 1]: {task.image_url}"]
    if task.image2_url:
        lines += [f"[Фото 2]: {task.image2_url}"]
    return "\n".join(lines)


def build_user_prompt(task: ExamTask, transcript: str) -> str:
    """
    Собирает user-message. ВНИМАНИЕ к порядку частей — от стабильного к уникальному.

    Возвращает один большой текст.
    """
    criteria = get_criteria(task.task_type)
    schema = _SCHEMA_BY_TASK_TYPE[task.task_type]

    parts = [
        "# Критерии оценивания",
        "",
        criteria,
        "",
        "---",
        "",
        _render_task_data(task),
        "",
        "---",
        "",
        "# Формат ответа",
        "",
        schema,
        "",
        "---",
        "",
        "# Транскрипт ответа ученика (от Whisper)",
        "",
        transcript.strip(),
        "",
        "---",
        "",
        "Оцени ответ ученика строго по критериям выше и верни JSON.",
    ]
    return "\n".join(parts)


def build_messages(task: ExamTask, transcript: str) -> list[dict]:
    """
    Собирает список messages для OpenAI Chat Completions.
    System всегда константа, user — собранный билдером.
    """
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": build_user_prompt(task, transcript)},
    ]
